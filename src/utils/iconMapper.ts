/**
 * Récupère le composant d'icône correspondant au nom
 * @param iconName - Nom de l'icône (ex: "FiTrendingUp")
 * @returns Composant d'icône React ou icône par défaut si non trouvée
 */
export function getIconComponent(iconName: string): IconType {
  return iconMap[iconName] || FiZap;
}

/**
 * Vérifie si un nom d'icône est valide
 * @param iconName - Nom de l'icône à vérifier
 * @returns true si l'icône existe, false sinon
 */
export function isValidIcon(iconName: string): boolean {
  return iconName in iconMap;
}

/**
 * Récupère la liste de toutes les icônes disponibles
 * @returns Tableau de noms d'icônes
 */
export function getAvailableIcons(): string[] {
  return Object.keys(iconMap);
}

