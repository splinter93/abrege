const Sidebar = ({ onToggleFoldersPanel }) => {
  const hoverTimeoutRef = useRef(null);

  const handleChevronMouseEnter = () => {
    // Démarre le timer de 250ms
    hoverTimeoutRef.current = setTimeout(() => {
      onToggleFoldersPanel(true);
    }, 250);
  };

  const handleChevronMouseLeave = () => {
    // Annule le timer si la souris quitte avant les 200ms
    if (hoverTimeoutRef.current) {
      clearTimeout(hoverTimeoutRef.current);
      hoverTimeoutRef.current = null;
    }
  };

  // Nettoyage du timeout au démontage du composant
  useEffect(() => {
    return () => {
      if (hoverTimeoutRef.current) {
        clearTimeout(hoverTimeoutRef.current);
      }
    };
  }, []);

  return (
    <aside className="sidebar">
      <div className="sidebar-main-content">
        {/* -- Bloc Navigation -- */}
        <div className="sidebar-block">
          <nav className="sidebar-nav">
            <Link href="/" className="nav-link">
              <HomeIcon />
              <span>Accueil</span>
            </Link>
            <Link href="/favoris" className="nav-link">
              <StarIcon />
              <span>Favoris</span>
            </Link>
          </nav>
        </div>

        {/* -- Bloc Organisation -- */}
        <div className="sidebar-block">
          <nav className="sidebar-nav">
            <div className="special-item">
              <Link href="/dossiers" className="nav-link">
                <FolderIcon />
                <span>Mes Dossiers</span>
              </Link>
              <button 
                className="sidebar-toggle-button"
                onMouseEnter={handleChevronMouseEnter}
                onMouseLeave={handleChevronMouseLeave}
              >
                <ChevronsRightIcon />
              </button>
            </div>
            <Link href="/agents" className="nav-link">
              <AgentIcon />
              <span>Mes Agents</span>
            </Link>
          </nav>
        </div>
        
        {/* -- Bloc Système -- */}
        <div className="sidebar-block">
          <nav className="sidebar-nav">
            <Link href="/parametres" className="nav-link">
              <SettingsIcon />
              <span>Paramètres</span>
            </Link>
          </nav>
        </div>
      </div>
      
      {/* -- Compte Utilisateur (tout en bas) -- */}
      <div className="sidebar-footer">
        <Link href="/compte" className="nav-link">
          <AccountIcon />
          <span>Mon Compte</span>
        </Link>
      </div>
    </aside>
  );
};

Sidebar.defaultProps = {
  onToggleFoldersPanel: () => {},
};

export default Sidebar; 